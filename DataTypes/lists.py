movies = ["Lion King", "Lagaan", "James Bond"]
print(movies)

# print each item by index
print(movies[0])
print(movies[1])
print(movies[2])

# print the last item
print(movies[-1])

# print the range of values
print(movies[0:2])

# use of insert
movies.insert(3, "Don")
print(movies)

# Append a value to list
movies.append("Hero")
print(movies)